
/**
 * Returns the nav bar visibility flag.
 */
export const selectNavbarVisible = (state) => state.views.navbarVisible;

/**
 * Returns the side bar visibility flag.
 */
export const selectSidebarVisible = (state) => state.views.sidebarVisible;

/**
 * Returns the small screen flag.
 */
export const selectSmallScreen = (state) => state.views.smallScreen;
